#include "3-calc.h"

/**
 * op_add - return sum of a & b
 * @a: The first num
 * @b: The second num
 * Return: Success
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - return diffrence of a & b
 * @a: The first num
 * @b: The second num
 * Return: Success
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - return the product of a & b
 * @a: The first num
 * @b: The second num
 * Return: Success
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - return the result of division of a & b
 * @a: The first num
 * @b: The second num
 * Return: Success
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - return the remainder of a & b
 * @a: The first num
 * @b: The second num
 * Return: Success
 */

int op_mod(int a, int b)
{
	return (a % b);
}
